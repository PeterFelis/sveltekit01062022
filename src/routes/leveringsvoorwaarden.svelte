    <p>Fetum b.v, Grote waard 36, Honselersdijk hierna te noemen: gebruiker</p>
    <p>
      <span class="font-bold">Artikel 1</span>
      Definities 1. <br />In deze algemene voorwaarden worden de hierna volgende termen in de
      navolgende betekenis gebruikt, tenzij uitdrukkelijk anders is aangegeven: <br />Gebruiker: de
      gebruiker van de algemene voorwaarden; <br />Koper: de wederpartij van gebruiker, handelend in
      de uitoefening van beroep of bedrijf;<br /> Overeenkomst: de overeenkomst tussen gebruiker en koper.
    </p>

    <p>
      <span class="font-bold">Artikel 2 Algemeen</span> 1. De bepalingen van deze algemene voorwaarden
      gelden voor iedere aanbieding, offerte en iedere overeenkomst tussen gebruiker en een koper waarop
      gebruiker deze voorwaarden van toepassing heeft verklaard, voor zover van deze voorwaarden niet
      door partijen uitdrukkelijk en schriftelijk is afgeweken. 2. De onderhavige voorwaarden zijn eveneens
      van toepassing op alle overeenkomsten met gebruiker, voor de uitvoering waarbij derden dienen te
      worden betrokken. 3. Toepasselijkheid van de algemene voorwaarden van de opdrachtgever is uitgesloten
      voor zover deze in strijd zijn met deze voorwaarden. 4. Indien een of meerdere bepalingen in deze
      algemene voorwaarden nietig zijn of vernietigd worden, blijven de overige bepalingen van deze algemene
      voorwaarden volledig van toepassing. Gebruiker en koper zullen in overleg nieuwe bepalingen ter
      vervanging van de nietige of vernietigde bepalingen overeenkomen, waarbij voor zover mogelijk het
      doel en de strekking van de oorspronkelijke bepaling in acht wordt genomen.
    </p>
    <p>
      <span class="font-bold">Artikel 3 Aanbiedingen en offertes</span> 1. Alle aanbiedingen en offertes
      zijn vrijblijvend, tenzij in het aanbod of in de offerte uitdrukkelijk anders is aangegeven. 2.
      Levertijden in offertes van de gebruiker zijn indicatief en geven de koper bij overschrijding ervan
      geen recht op ontbinding of schadevergoeding, tenzij uitdrukkelijk anders is overeengekomen. 3.
      Gebruiker is niet gebonden aan de aanvaarding indien deze afwijkt van het in de aanbieding of offerte
      opgenomen aanbod. 4. Door gebruiker aan de wederpartij bij of na offerte ter kennis gebrachte afbeeldingen,
      technische specificaties en overige gegevens, opgenomen in brochures, prijslijsten, e.d., hebben
      het karakter van een benaderende aanduiding en verbinden slechts indien uitdrukkelijk overeengekomen.
      5. De in de aanbieding of offerte genoemde prijzen zijn exclusief BTW en andere heffingen van overheidswege,
      alsmede transportkosten, tenzij uitdrukkelijk anders is vermeld. 6. Aanbiedingen of offertes gelden
      niet automatisch voor nabestellingen.
    </p>
    <p>
      <span class="font-bold">Artikel 4 Aanvang en uitvoering overeenkomst</span> 1. Een overeenkomst
      wordt geacht tot stand te zijn gekomen wanneer de desbetreffende opdracht schriftelijk door gebruiker
      is bevestigd, dan wel gebruiker met de uitvoering een aanvang heeft genomen. 2. Gebruiker zal de
      overeenkomst naar beste inzicht en vermogen en overeenkomstig de eisen van goed vakmanschap uitvoeren.
      3. Indien en voor zover een goede uitvoering van de overeenkomst dit vereist, heeft gebruiker het
      recht bepaalde werkzaamheden te laten verrichten door derden. 4. De koper draagt zorg voor de tijdige
      verstrekking van alle gegevens, waarvan gebruiker aangeeft dat deze noodzakelijk zijn of waarvan
      de koper redelijkerwijs behoort te begrijpen dat deze noodzakelijk zijn voor het uitvoeren van
      de overeenkomst. Indien de voor de uitvoering van de overeenkomst benodigde gegevens niet tijdig
      aan gebruiker zijn verstrekt, heeft gebruiker het recht om de uitvoering van de overeenkomst op
      te schorten en de uit de vertraging voortvloeiende extra kosten volgens de gebruikelijke tarieven
      aan de koper in rekening te brengen. 5. Indien is overeengekomen dat de overeenkomst in fasen zal
      worden uitgevoerd, kan gebruiker de uitvoering van die onderdelen die tot een volgende fase behoren
      opschorten tot dat de koper de resultaten van de daaraan voorafgaande fase schriftelijk heeft goedgekeurd.
      6. Indien door gebruiker of door gebruiker ingeschakelde derden in het kader van de opdracht installatie-
      of onderhouds-werkzaamheden worden verricht op de locatie van koper of een door koper aangewezen
      locatie, draagt koper kosteloos zorg voor de door die medewerkers in redelijkheid gewenste faciliteiten.
    </p>
    <p>
      <span class="font-bold">Artikel 5 Wijziging van de overeenkomst </span>1. Indien tijdens de
      uitvoering van de overeenkomst blijkt dat voor een behoorlijke uitvoering het noodzakelijk is
      om de te verrichten werkzaamheden te wijzigen of aan te vullen, zullen partijen tijdig en in
      onderling overleg de overeenkomst dienovereenkomstig aanpassen. 2. Indien partijen
      overeenkomen dat de overeenkomst wordt gewijzigd of aangevuld, kan het tijdstip van voltooiing
      van de uitvoering daardoor worden beïnvloed. Gebruiker zal de koper zo spoedig mogelijk
      hiervan op de hoogte stellen. 3. Indien een vast tarief is overeengekomen, zal gebruiker
      daarbij aangeven in hoeverre de wijziging of aanvulling van de overeenkomst een overschrijding
      van dit vast tarief tot gevolg heeft.
    </p>
    <p>
      <span class="font-bold">Artikel 6 Levering</span> 1. Koper is verplicht de zaken af te nemen op
      het moment dat gebruiker deze bij hem aflevert of doet afleveren, dan wel op het moment waarop
      deze hem volgens de overeenkomst ter beschikking worden gesteld. 2. Indien de koper afname weigert
      of nalatig is met het verstrekken van informatie of instructies die noodzakelijk zijn voor de levering,
      is gebruiker gerechtigd de zaken op te slaan voor rekening en risico van koper. 3. Indien gebruiker
      gegevens behoeft van de koper in het kader van uitvoering van de overeenkomst, vangt de levertijd
      aan nadat koper deze aan gebruiker ter beschikking heeft gesteld. 4. Indien gebruiker een leveringstermijn
      heeft opgegeven, is deze indicatief en nimmer een fatale termijn. Bij overschrijding van een termijn
      dient de koper gebruiker schriftelijk in gebreke te stellen. 5. Gebruiker is gerechtigd de zaken
      in delen te leveren, tenzij hiervan bij overeenkomst is afgeweken of aan de deellevering geen zelfstandige
      waarde toekomt. Gebruiker is gerechtigd het aldus geleverde afzonderlijk te factureren.
    </p>
    <p>
      <span class="font-bold">Artikel 7 Monsters en modellen</span> 1. Is aan de koper een monster of
      model getoond of verstrekt, dan wordt het vermoed slechts als aanduiding te zijn verstrekt zonder
      dat de zaak daaraan hoeft te beantwoorden, tenzij uitdrukkelijk wordt overeengekomen dat de zaak
      daarmee zal overeenstemmen. 2. Een aan de koper verstrekt monster of model blijft eigendom van
      gebruiker.
    </p>
    <p>
      <span class="font-bold">Artikel 8 Onderzoek, reclames</span> 1. Koper is gehouden het geleverde
      op het moment van (af)levering, doch in ieder geval binnen zo kort mogelijke termijn te onderzoeken
      om te bepalen of de kwaliteit en kwantiteit van het geleverde overeenstemt met hetgeen is overeengekomen,
      althans voldoet aan de eisen die daaraan in het normale (handels) verkeer gelden. 2. Eventuele
      zichtbare gebreken of tekorten behoren binnen drie dagen na levering schriftelijk aan gebruiker
      te worden gemeld. Niet zichtbare gebreken of tekorten behoren binnen drie weken na ontdekking doch
      uiterlijk binnen 12 maanden na levering te worden gemeld. 3. Indien conform lid 2, tijdig wordt
      gereclameerd, blijft koper verplicht tot afname en betaling van de gekochte zaken. Wenst koper
      gebrekkige zaken te retourneren, dan geschiedt zulks met voorafgaande schriftelijke toestemming
      van de gebruiker op de wijze zoals door gebruiker aangegeven.
    </p>
    <p>
      <span class="font-bold">Artikel 9 Vergoedingen, prijs en kosten</span> 1. Gebruiker mag prijsstijgingen
      doorberekenen, indien tussen het moment van aanbieding en uitvoering van de overeenkomst zich naar
      het oordeel van gebruiker significante en voor de branche van gebruiker relevante prijswijzigingen
      hebben voorgedaan ten aanzien van bijvoorbeeld wisselkoersen, lonen, inkoopprijzen, verpakkingsmateriaal.
      Dit geldt tevens voor servicecontracten. 2. De door gebruiker gehanteerde prijzen zijn exclusief
      BTW en eventuele andere heffingen, alsmede eventuele in het kader van de overeenkomst te maken
      kosten, waaronder verzend- en administratiekosten, tenzij anders aangegeven.
    </p>

    <p>
      <span class="font-bold">Artikel 10 Opschorting en ontbinding </span> 1. Gebruiker is bevoegd de
      nakoming van de verplichtingen op te schorten of de overeenkomst te ontbinden, indien: - koper
      de verplichtingen uit de overeenkomst niet of niet volledig nakomt. - Na het sluiten van de overeenkomst
      aan gebruiker ter kennis gekomen omstandigheden goede grond geven te vrezen dat de koper de verplichtingen
      niet of slechts deels zal nakomen. In dit geval is de opschorting toegelaten voor zover de tekortkoming
      haar rechtvaardigt. - Koper bij het sluiten van de overeenkomst verzocht is zekerheid te stellen
      voor de voldoening van zijn verplichtingen uit de overeenkomst en deze zekerheid uitblijft of onvoldoende
      is. Zodra zekerheid is gesteld, vervalt de bevoegdheid tot opschorting, tenzij deze voldoening
      daardoor onredelijk is vertraagd. - Koper of een derde zonder uitdrukkelijke schriftelijke toestemming
      van gebruiker wijzigingen aan de door de gebruiker geleverde producten hebben aangebracht, danwel
      getracht hebben dit te doen. - De producten niet volgens de voorschriften van de fabrikant worden
      gebruikt. Dit geldt met name voor de vochtigheidsgraad en temperatuur. 2. Voorts is gebruiker bevoegd
      de overeenkomst te ontbinden indien zich omstandigheden voordoen welke van dien aard zijn dat nakoming
      van de overeenkomst onmogelijk blijkt of naar maatstaven van redelijkheid en billijkheid niet langer
      kan worden gevergd. 3. Indien de overeenkomst wordt ontbonden, zijn de vorderingen van gebruiker
      op de koper onmiddellijk opeisbaar. 4. Indien gebruiker de nakoming van de verplichtingen opschort,
      behoudt hij zijn aanspraken uit de wet en overeenkomst. 5. Gebruiker behoudt steeds het recht schadevergoeding
      te vorderen.
    </p>
    <p>
      <span class="font-bold">Artikel 11 Betaling </span>1. Betaling dient te geschieden binnen tien
      dagen na factuurdatum, op een door gebruiker aan te geven wijze in de valuta waarin is
      gefactureerd. Bezwaren tegen de hoogte van factuurbedragen, schorten de betalingsverplichting
      niet op. Elk beroep aan zijde van de koper op verrekening is uitgesloten. 2. Indien koper in
      gebreke blijft in de betaling binnen de termijn van 10 dagen, dan is de koper van rechtswege
      in verzuim. Koper is alsdan een rente verschuldigd van 1% per maand, tenzij de wettelijke
      rente hoger is in welk geval de wettelijke rente geldt. De rente over het opeisbaar bedrag zal
      worden berekend vanaf het moment dat koper in verzuim is tot het moment van voldoening van het
      volledige bedrag. 3. In geval van liquidatie, faillissement, beslag of surséance van betaling
      van de koper, zijn de vorderingen van gebruiker op de koper onmiddellijk opeisbaar. 4.
      Gebruiker heeft het recht de door de koper gedane betalingen te laten strekken in de eerste
      plaats in mindering van de kosten, vervolgens in mindering van de opengevallen rente en
      tenslotte in mindering van de hoofdsom en de lopende rente. Gebruiker kan, zonder daardoor in
      verzuim te komen, een aanbod tot betaling weigeren, indien de koper een andere volgorde voor
      de toerekening aanwijst. Gebruiker kan volledige aflossing van de hoofdsom weigeren, indien
      daarbij niet eveneens de opengevallen en lopende rente alsmede de kosten worden voldaan.
    </p>
    <p>
      <span class="font-bold">Artikel 12 Eigendomsvoorbehoud </span>1. Alle door gebruiker geleverde
      zaken, daaronder mede begrepen eventuele ontwerpen, software, (electronische) bestanden, enz.,
      blijven eigendom van gebruiker totdat de koper alle verplichtingen uit hoofde van alle met
      gebruiker gesloten overeenkomsten, is nagekomen. 2. De koper is niet bevoegd de onder het
      eigendomsvoorbehoud vallende zaken te verpanden noch op enige andere wijze te bezwaren. 3.
      Indien derden beslag leggen op de onder eigendomsvoorbehoud geleverde zaken dan wel rechten
      daarop willen vestigen of doen gelden, is koper verplicht gebruiker zo snel als redelijkerwijs
      verwacht mag worden daarvan op de hoogte te stellen. 4. De koper verplicht zich de onder
      eigendomsvoorbehoud geleverde zaken te verzekeren en verzekerd te houden tegen brand,
      ontploffing- en waterschade alsmede tegen diefstal . 5. Voor het geval dat gebruiker zijn in
      dit artikel aangeduide eigendomsrechten wil uitoefenen, geeft de koper reeds nu
      onvoorwaardelijke en niet herroepbare toestemming aan gebruiker of door deze aan te wijzen
      derden om al die plaatsen te betreden waar de eigendommen van gebruiker zich bevinden en die
      zaken mede terug te nemen.
    </p>
    <p>
      <span class="font-bold">Artikel 13 Garantie </span>1. Gebruiker garandeert dat de te leveren
      zaken voldoen aan de gebruikelijke eisen en normen die daaraan kunnen worden gesteld en vrij
      zijn van welke gebreken ook. 2. Voor alle door gebruiker geleverde producten, uitgezonderd
      televisiesystemen, hoofdtelefoons en batterijen, geldt de in artikel 13.1 genoemde garantie
      voor een periode van 12 maanden na levering. Voor televisiesystemen geldt de in het
      bijbehorende servicecontract genoemde garantietermijn. 3. Bovengenoemde garantie geldt niet
      wanneer: - het gebrek is ontstaan als gevolg van onoordeelkundig of oneigenlijk gebruik. -
      zonder schriftelijke toestemming van gebruiker, koper of derden wijzigingen hebben aangebracht
      of hebben getracht aan te brengen aan de zaak. - de in artikel 14.2 genoemde producten zijn
      aangewend voor doeleinden waarvoor de zaak niet bestemd is. - Schade veroorzaakt door
      incorrecte, niet conform de fabrieksvoorschriften, installatie van de zaak. - schade is
      ontstaan door water, brand, bliksem en terrorisme. - deze producten niet volgens de
      voorschriften van de fabrikant worden gebruikt. Dit geldt met name voor de vochtigheidsgraad
      en temperatuur. 4. Indien de te leveren zaken niet voldoen aan deze garanties, zal gebruiker
      de zaak binnen redelijke termijn na melding door koper, herstellen of vervangen. In geval van
      vervanging verbindt koper zich reeds nu de vervangen zaak, alsmede het eigendom, aan gebruiker
      te verschaffen. 5. Indien de door gebruiker verstrekte garantie een zaak betreft die door een
      derde is geproduceerd, is de garantie beperkt tot hetgeen door de producent van de zaak
      daarvoor wordt verstrekt.
    </p>
    <p>
      <span class="font-bold">Artikel 14 Incassokosten</span>1. Is koper in gebreke of in verzuim
      met het nakomen van één of meer van zijn verplichtingen, dan komen alle redelijke kosten ter
      verkrijging van voldoening buiten rechte voor rekening van koper. Indien koper in gebreke
      blijft in de tijdige voldoening van een geldsom, dan verbeurt hij een dadelijk opeisbare boete
      van 15% over het nog verschuldigde bedrag, met een minimum van € 50,00. 2. Indien gebruiker
      hogere kosten heeft gemaak, welke redelijkerwijs noodzakelijk waren, komen ook deze voor
      vergoeding in aanmerking. 3. De eventuele gemaakte redelijke gerechtelijke en executiekosten
      komen eveneens voor rekening van koper. 4. Koper is over de gemaakte incassokosten rente
      verschuldigd.
    </p>
    <p>
      <span class="font-bold">Artikel 15 Aansprakelijkheid</span> 1 Indien door gebruiker geleverde zaken
      gebrekkig zijn, is de aansprakelijkheid van gebruiker jegens koper beperkt tot hetgeen in deze
      voorwaarden onder "Garantie" is geregeld. 2. Indien en voor zover er op gebruiker enige aansprakelijkheid
      rust, uit welke hoofde ook, is deze aansprakelijkheid ten alle tijde beperkt tot maximaal het door
      de aansprakelijkheidsverzekering toegekende bedrag. 3. Gebruiker is nimmer aansprakelijk voor indirecte
      schade, daaronder begrepen gevolgschade, gederfde winst, gemiste besparingen en schade door bedrijfsstagnatie,
      alsmede schade geleden door derden. 4. Gebruiker is niet aansprakelijk voor schade, van welke aard
      ook, doordat gebruiker is uit gegaan van door de koper verstrekte onjuiste of onvolledige gegevens,
      tenzij deze onjuistheid of onvolledigheid voor gebruiker kenbaar behoorde te zijn. 5. De koper
      vrijwaart gebruiker voor aanspraken van derden met betrekking tot rechten van intellectuele eigendom
      op door de koper verstrekte materialen of gegevens, die bij de uitvoering van de overeenkomst worden
      gebruikt. 6. De in deze voorwaarden opgenomen beperkingen van de aansprakelijkheid voor directe
      schade gelden niet indien de schade enkel of voor zover, is te wijten aan opzet of grove schuld
      van gebruiker of zijn ondergeschikten. 7. Voorwaarde voor het ontstaan van enig recht op schadevergoeding
      is dat de koper de schade binnen 14 dagen na het ontstaan daarvan, schriftelijk bij de gebruiker
      meldt.
    </p>
    <p>
      <span class="font-bold">Artikel 16 Risico-overgang</span> 1. Het risico van verlies of beschadiging
      van producten die voorwerp van de overeenkomst zijn, gaat op koper over op het moment waarop deze
      aan koper juridisch of feitelijk worden geleverd en daarmee in de macht van koper of van een door
      koper aan te wijzen derde worden gebracht.
    </p>
    <p>
      <span class="font-bold">Artikel 17 Overmacht </span>1. Partijen zijn niet gehouden tot het
      nakomen van enige verplichting, indien zij daartoe gehinderd worden als gevolg van een
      omstandigheid die niet is te wijten aan schuld, en noch krachtens de wet, een rechtshandeling
      of in het verkeer geldende opvattingen voor hun rekening komt. 2. Onder overmacht wordt in
      deze algemene voorwaarden verstaan, naast hetgeen daaromtrent in de wet en jurisprudentie
      wordt begrepen, alle van buiten komende oorzaken, voorzien of niet voorzien, waarop gebruiker
      geen invloed kan uitoefenen, doch waardoor gebruiker niet in staat is zijn verplichtingen na
      te komen. Werkstakingen in het bedrijf van gebruiker worden daaronder begrepen. 3. Gebruiker
      heeft ook het recht zich op overmacht te beroepen, indien de omstandigheid die (verdere)
      nakoming verhindert, intreedt nadat gebruiker zijn verbintenis had moeten nakomen. 4. Partijen
      kunnen gedurende de periode dat de overmacht voortduurt, de verplichtingen uit de overeenkomst
      opschorten. Indien deze periode langer duurt dan twee maanden, is ieder der partijen
      gerechtigd de overeenkomst te ontbinden zonder verplichting tot schadevergoeding aan de andere
      partij. 5. Voor zoveel gebruiker ten tijde van het intreden van overmacht inmiddels
      gedeeltelijk zijn verplichtingen uit de overeenkomst is nagekomen of deze zal kunnen nakomen,
      en aan het nagekomen respectievelijk na te komen gedeelte zelfstandige waarde toekomt, is
      gebruiker gerechtigd om het reeds nagekomen respectievelijk na te komen gedeelte separaat te
      factureren. Koper is gehouden deze factuur te voldoen als ware het een afzonderlijk
      overeenkomst.
    </p>
    <p>
      <span class="font-bold">Artikel 18 Intellectuele eigendom en auteursrechten</span> 1. Alle rechten
      van intellectuele of industriële eigendom op alle krachtens de overeenkomst ontwikkelde of ter
      beschikking gestelde programmatuur, apparatuur of andere materialen zoals offertes, documentatie
      en ontwerpen, alsmede voorbereidend materiaal daarvan, berusten uitsluitend bij gebruiker of diens
      licentiegever. De koper krijgt uitsluitend de gebruiksrechten en bevoegdheden die bij deze voorwaarden
      of anderszins uitdrukkelijk worden toegekend en voor het overige zal hij de programmatuur of andere
      materialen niet verveelvoudigen of daarvan kopieën vervaardigen. 2. De koper verbindt zich deze
      programmatuur, apparatuur en bijbehorende materialen geheim te houden, niet aan derden bekend te
      maken of in gebruik te geven en slechts te gebruiken voor het doel waarvoor deze de koper ter beschikking
      zijn gesteld. 3. Het is de koper niet toegestaan veranderingen in de zaken aan te brengen, tenzij
      schriftelijk anders is overeengekomen. 4. Indien gebruiker aan koper gebruiksrechten en bevoegdheden
      verleent, gelden deze voor de duur van de overeenkomst. 5. Gebruiker behoudt het recht de eventueel
      door de uitvoering van de werkzaamheden toegenomen kennis voor andere doeleinden te gebruiken,
      voorzover hierbij geen vertrouwelijke informatie ter kennis van derden wordt gebracht.
    </p>
    <p>
      <span class="font-bold">Artikel 19 Geheimhouding </span>1. Beide partijen zijn verplicht tot
      geheimhouding van alle vertrouwelijke informatie die zij in het kader van hun overeenkomst van
      elkaar of uit andere bron hebben verkregen. Informatie geldt als vertrouwelijk als dit door
      een partij is medegedeeld of als dit voortvloeit uit de aard van de informatie. 2. Indien, op
      grond van een wettelijke bepaling of een rechterlijke uitspraak, gebruiker gehouden is
      vertrouwelijke informatie aan door de wet of de bevoegde rechter aangewezen derden mede te
      verstrekken, en gebruiker zich ter zake niet kan beroepen op een wettelijk dan wel door de
      bevoegde rechter erkend of toegestaan recht van verschoning, dan is gebruiker niet gehouden
      tot schadevergoeding of schadeloosstelling en is de wederpartij niet gerechtigd tot ontbinding
      van de overeenkomst op grond van enige schade, hierdoor ontstaan.
    </p>
    <p>
      <span class="font-bold">Artikel 20 Geschillen </span>1. De rechter in de vestigingsplaats van
      gebruiker is bij uitsluiting bevoegd van geschillen kennis te nemen, tenzij de kantonrechter
      bevoegd is. Niettemin heeft gebruiker het recht het geschil voor te leggen aan de volgens de
      wet bevoegde rechter. 2. Partijen zullen een beroep op de rechter doen nadat zij zich tot het
      uiterste hebben ingespannen een geschil in onderling overleg te beslechten. Artikel 21
      Toepasselijk recht 1. Op elke overeenkomst tussen gebruiker en de koper is Nederlands recht
      van toepassing. Het Weens Koopverdrag is uitdrukkelijk uitgesloten Artikel 22 Wijziging,
